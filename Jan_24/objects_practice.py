import turtle

theScreen = turtle.Screen()

# Control how big we want the screen to be
theScreen.setup(height=600, width=1200)

# Customize its background color
theScreen.bgcolor('green')

# Control what is written on the top of the window
theScreen.title('Hi Yixin')

turt = turtle.Turtle()
turt.goto()
turt.up()
turt.down()

turt.shape('turtle')
turt.shape('arrow')

turt.pensize(10)